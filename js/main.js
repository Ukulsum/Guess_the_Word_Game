let inputs = document.querySelector(".inputs");
let reset_btn = document.querySelector(".reset_btn");
let hints = document.querySelector(".hint span");
let typing_input = document.querySelector(".typing_input");
let wrong_letter = document.querySelector(".wrong_letter span");
let guesses = document.querySelector(".guesses span")


let word, maxGuesses, corrects = [], incorrects = [];

function randomWord(){
//getting random object form wordlist
    let random_object = wordList[Math.floor(Math.random() * wordList.length)];
  //getting word of random object
     word = random_object.word;
     maxGuesses = 8; corrects = []; incorrects = [];

     hints.innerHTML = random_object.hint;
     guesses.innerHTML = maxGuesses;
     wrong_letter.innerHTML = incorrects;

 
  //create inputs tag according to the word length
    let html = "";
    for(let i = 0; i < word.length; i++){
        html += `<input type="text" disabled>`;
    }
    inputs.innerHTML = html; 

  //show the hint of the word
    hints.innerHTML = random_object.hint;
}


//calling random object from the wordlist
randomWord();

function initGame(e){
    let key = e.target.value;
 //validate only user pressed key is alphabet character
      if(key.match(/^[A-Za-z]+$/) && !incorrects.includes(` ${key} `) && !corrects.includes(key)){
       // if  user letter founded in the word
           if(word.includes(key)){
         //show the found letter in input
             for(let i = 0; i < word.length; i++){
          // showing match letter in the input value
              if(word[i] === key){
                corrects.push(key);
                inputs.querySelectorAll("input")[i].value = key;
               }
           } 
      }else{
        //decrement maxGuesses by 1
        maxGuesses--;
        incorrects.push(` ${key} `);
     }
     guesses.innerHTML = maxGuesses;
     wrong_letter.innerHTML = incorrects;
    }
  //empty the input tag once user entered any key
    typing_input.value = "";

    setTimeout(() => {
        //if user found all letters
    if(corrects.length === word.length){
        alert(`Congrats! You found the word ${word.toUpperCase()}`);
       //calling randomword func, so the game reset
       randomWord();
    }
  //if user couldn't found all letters
    if(maxGuesses < 1){
          alert("Game is over! You don't have remaining guesses");
          for(let i = 0; i < word.length; i++){
             // show all letter in the input 
                inputs.querySelectorAll("input")[i].value = word[i];
              } 
     }
    })
}
//get the random word on the button click
reset_btn.addEventListener("click", randomWord);
typing_input.addEventListener("input", initGame);
//Automatically focusing input when user pressed any key
document.addEventListener("keydown", () => typing_input.focus());
